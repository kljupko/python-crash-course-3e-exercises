places = ["tokyo", "grand canyon", "iceland", "victoria falls", "machu picchu"]
print(places)

print("\nSorted:")
print(sorted(places))

print("\nOriginal:")
print(places)

print("\nSorted in reverse:")
print(sorted(places, reverse = True))

print("\nOriginal:")
print(places)

print("\nReverse:")
places.reverse()
print(places)

print("\nre-Reverse:")
places.reverse()
print(places)

print("\nSort original:")
places.sort()
print(places)

print("\nSort original in reverse:")
places.sort(reverse = True)
print(places)
